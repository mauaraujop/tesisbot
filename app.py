@app.route('/whatsapp', methods=['POST'])
def ReceivedMessage():
    try:
        body = request.get_json()
        
        # Imprime el cuerpo completo para depuraci√≥n (opcional, puedes quitarlo despu√©s) (CAMBIO: A√±adido print para depuraci√≥n)
        print(f"Cuerpo del mensaje recibido: {body}")

        # Extrae la informaci√≥n del mensaje
        entry = (body["entry"])[0]
        changes = (entry["changes"])[0]
        value = changes["value"]
        
        # Aseg√∫rate de que haya mensajes en el valor (CAMBIO: A√±adida verificaci√≥n de 'messages' en el payload)
        if "messages" not in value:
            print("No hay mensajes en el valor recibido.") # (CAMBIO: A√±adido print para depuraci√≥n)
            return "EVENT_RECEIVED" # Devuelve 200 OK para WhatsApp

        message = (value["messages"])[0]
        number = message["from"]
        
        # --- CAMBIO CLAVE: Manejo de diferentes tipos de mensajes ---
        text = "" # Inicializa text
        if message["type"] == "text":
            text = util.GetTextUser(message)
            print(f"DEBUG: Mensaje de texto recibido de {number}: '{text}'")
        elif message["type"] == "interactive":
            # Si es un mensaje interactivo, extrae el t√≠tulo de la opci√≥n seleccionada
            if "list_reply" in message["interactive"]:
                text = message["interactive"]["list_reply"]["title"]
                print(f"DEBUG: Opci√≥n de lista interactiva recibida de {number}: '{text}'")
            elif "button_reply" in message["interactive"]:
                text = message["interactive"]["button_reply"]["title"]
                print(f"DEBUG: Opci√≥n de bot√≥n interactivo recibida de {number}: '{text}'")
            else:
                print(f"DEBUG: Tipo de mensaje interactivo no reconocido: {message['interactive']['type']}")
                # Puedes enviar un mensaje de error o simplemente ignorarlo
                return "EVENT_RECEIVED"
        else:
            # Manejar otros tipos de mensajes (ej. im√°genes, audios)
            print(f"DEBUG: Tipo de mensaje no soportado: {message['type']}")
            # Opcional: enviar un mensaje al usuario diciendo que no soportas ese tipo de mensaje
            data = util.TextMessage("Lo siento, solo puedo responder a mensajes de texto y opciones de men√∫ por ahora.", number)
            whatsappservice.SendMessageWhatsapp(data)
            return "EVENT_RECEIVED" # No procesar m√°s si el tipo no es texto/interactivo
        # --- FIN CAMBIO CLAVE ---

        # 1. Intentar procesar el mensaje con la l√≥gica de palabras clave
        # La funci√≥n ProcessMessage ahora devuelve True si maneja el mensaje, False si no.
        message_handled_by_keywords = ProcessMessage(text, number)

        # 2. Si las palabras clave no manejaron el mensaje, entonces llamar al modelo de lenguaje (Groq)
        if not message_handled_by_keywords:
            print(f"DEBUG: No se encontr√≥ palabra clave. Llamando al modelo de lenguaje para: {text}")
            responseLLM = chatgptservice.GetResponse(text)
            
            if responseLLM != "error":
                data = util.TextMessage(responseLLM, number)
            else:
                data = util.TextMessage("Lo siento, ocurri√≥ un problema al obtener la respuesta del asistente. Por favor, int√©ntalo de nuevo m√°s tarde.", number)
            
            whatsappservice.SendMessageWhatsapp(data)

        return "EVENT_RECEIVED"
    except Exception as e:
        # Captura cualquier excepci√≥n no manejada y la imprime para depuraci√≥n
        print(f"Error general en ReceivedMessage: {e}")
        return "EVENT_RECEIVED" # Siempre devuelve 200 OK para WhatsApp, incluso en error

